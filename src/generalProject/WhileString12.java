package generalProject;

public class WhileString12 {
    public static void main(String[] args) {
        String str = "$150k/year Job Offer ";
        int month = 1;
        while (month <= 5) {
            System.out.println(str + month);
            month++;
        }
        System.out.println("Offer from Airbnb Received!");
    }

}

